#include "tokenize_utils.h"

bool	is_metacharacter(char c)
{
	return (c == ' '
		|| c == '\t'
		|| c == '<'
		|| c == '\''
		|| c == '\"'
		|| c == '$'
		|| c == '>'
		|| c == '('
		|| c == ')'
		|| c == '&'
		|| c == '|');
}
